package peaksoft;

public class Mushuk {
    private String name;
    private int age;

    public Mushuk(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Mushuk() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
